PRODUCT U1.1.4 ONTWIKKELING 
Binnen het pakket is een differentiatie aangebracht in een uniform deel en een maatwerkdeel . 
Toelichting 
Het volgen van de ontwikkeling van kinderen en jongeren behoort tot de belangrijkste standaardactiviteiten in de jeugdgezondheidszorg . 
Voor het screenen van spraak- en taalontwikkingsstoornissen bij kleuters bestaat een landelijk gebruikte standaard . 
Deze activiteit is ondergebracht bij ' Screeningen ' . 
Voor de jongere leeftijdsgroep ( zuigelingen en peuters ) fungeert het Van_Wiechenonderzoek momenteel als landelijke standaard die op alle consultatiebureaus wordt gebruikt 3 . 
Het Van_Wiechenonderzoek bestrijkt de activiteiten U1.1.4.1 tot_en_met U1.1.4.4 en in geringere mate U1.1.4.5 . 
Voor peuters is een screeningsmethode voor de opsporing van spraak- en taalontwikkelingsstoornissen ontwikkeld , maar deze is nog niet landelijk geïmplementeerd , waardoor vooralsnog moet worden teruggevallen op het Van_Wiechenonderzoek . 
Het maakt daarmee een einde aan ongewenste verschillen in de uitvoering van de JGZ 0-19 jaar , maar schept anderzijds ruimte voor een gerichte variatie op lokaal niveau . 
Voor kinderen en jongeren vanaf de schoolleeftijd zijn diverse al_dan_niet gestandaardiseerde onderzoeken / testen ontwikkeld of in ontwikkeling , zoals de Child_Behaviour_Check_List ( CBCL ) 2-3 en 4-18 , de Landelijke_Signaleringshulp_Psychosociale_Problemen_Kleuters ( LSPPK ) , de Youth_Self_Report ( YSR ) en de Korte_Indicatieve_Vragenlijst_Psychosociale_problematiek_Adolescenten ( KIVPA ) . 
Er is nog geen consensus over het landelijk gebruik van deze onderzoeken / testen . 
Ervan uitgaande dat die consensus wel bereikt kan zijn op het moment waarop de AMvB van_kracht wordt , is ook het ontwikkelingsonderzoek bij kinderen ouder dan 4 jaar vooralsnog in het uniform deel opgenomen . 
De seksuele ontwikkeling wordt gevolgd aan_de_hand_van de op de groeidiagrammen afgedrukte ontwikkelingsstadia . 
Daarmee geldt het Integraal_Dossier_JGZ voor dit onderdeel als standaard . 
