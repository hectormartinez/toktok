Sinds ongeveer 1960 wordt landelijk de Ewingtest en de daarvan afgeleide geautomatiseerde CAPAS-test * gebruikt voor het screenen van negen maanden oude zuigelingen op de aanwezigheid van gehoorstoornissen . 
