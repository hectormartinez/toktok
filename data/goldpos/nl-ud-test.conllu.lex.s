PRODUCT U3.1.2 VACCINATIES 
Toelichting 
Het Rijksvaccinatieprogramma ( RVP ) wordt in_de_loop_van 2001 uitgebreid met een vaccinatie tegen kinkhoest bij vierjarige kinderen . 
Daardoor krijgen niet alle kinderen optimale zorg en worden sommige kinderen helemaal niet bereikt . 
Hiervoor wordt een zogenaamd a-cellulair kinkhoestvaccin gebruikt . 
Het RVP voorziet daarmee dan in de volgende vaccinaties . 
De discussie of de vaccinatie tegen hepatitis B opgenomen moet worden in het RVP is nog niet afgerond . 
Evenals de BCG-vaccinatie ** tegen tuberculose , wordt de vaccinatie tegen hepatitis B momenteel uitsluitend aan risicogroepen gegeven . 
Op_basis_van dit laatste zou de hepatitis B vaccinatie strikt genomen in het maatwerkdeel moeten worden ondergebracht . 
Echter , de daaruit voortvloeiende beleidsvrijheid voor gemeenten is ongewenst , omdat die zou kunnen leiden tot onaanvaardbare gezondheidsrisico's . 
Voor de uitvoering van vaccinaties , zowel in_het_kader_van het RVP als daarbuiten , geldt een door de Inspectie_voor_de_Gezondheidszorg gesanctioneerd handboek als landelijke standaard 6 . 
Toekomstige wijzigingen van het RVP moeten bij_voorbaat tot het uniform deel van het Basistakenpakket_Jeugdgezondheidszorg worden gerekend . 
Zie ook product U1.2.1.2 Vaccinatiegraad . 
Dat moet voorkomen worden . 
