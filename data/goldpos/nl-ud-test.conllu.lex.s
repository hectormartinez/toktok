Ad U1.2.1.1 Biometrie 
Het volgen van de lichamelijk groei van het individu wordt tot het uniform deel van het Basistakenpakket_Jeugdgezondheidszorg gerekend ( product U1.1.3 ) . 
Dit geldt ook voor de monitoring van groei op populatieniveau . 
De professional kan echter met het voorliggende basistakenpakket aan het werk . 
Dit gebeurt echter buiten het kader van de normale JGZ-activiteiten , namelijk in de vorm van landelijke groeistudies die sinds 1955 vier maal zijn uitgevoerd , voor_het_laatst in 1997 . 
Hoewel de frequentie van deze monitoring laag is , wordt zij wel tot het uniform deel van het Basistakenpakket_Jeugdgezondheidszorg gerekend . 
Voor monitoring van de individuele groei is een landelijke standaard beschikbaar 5 en worden de groeidiagrammen , die opgenomen zijn in het Integraal_Dossier_JGZ op landelijke schaal gebruikt . 
De groeidiagrammen vormen de grafische weergave van de resultaten van de meest recente landelijke groeistudie . 
Hiermee vormt het Integraal_Dossier_JGZ de landelijke standaard voor het monitoren van de groei op individueel niveau . 
Voor monitoring van de groei van de totale populatie moet het protocol van de Vierde_Landelijke_Groeistudie de blauwdruk zijn voor de vijfde 4 . 
Ad U1.2.1.2 Vaccinatiegraad 
De monitoring van de vaccinatiegraad gebeurt op het niveau van gemeenten door Provinciale_Ent_Administraties ( PEA's ) . 
Zij leveren jaarlijks de gegevens geaggregeerd aan bij de Inspectie_voor_de_Gezondheidszorg . 
Jaarlijkse rapportage vindt plaats op landelijk , provinciaal en gemeentelijk niveau door de Inspectie . 
Aan monitoring van de vaccinatiegraad op een lager niveau dan de gemeente ( wijk , buurt , school ) wordt veel belang gehecht ter preventie en ter bestrijding van epidemieën . 
Het is een belangrijke bijdrage aan een verdere verbetering van de gezondheid van de jeugd in ons land . 
Ad U1.2.1.3 Verwijzingen en U1.2.1.4 Vóórkomen van ziekten en afwijkingen 
Het monitoren van verwijzingen en het vóórkomen van ziekten en afwijkingen op regionaal en lokaal niveau is noodzakelijk om beleid te kunnen voeren en rechtvaardigt opneming in het uniform deel van het Basistakenpakket_Jeugdgezondheidszorg . 
Zodra een elektronische vorm van het Integraal_Dossier_JGZ beschikbaar komt , kan deze monitoring ­ na uniformering en protocollering ­ op een efficiënte wijze worden uitgevoerd . 
