Productgroep 4 : Voorlichting , advies , instructie en begeleiding 
PRODUCT M4.1.1 VOORLICHTING 
Toelichting 
Voorlichting wordt individueel afgestemd op de behoefte van de cliënt . 
Het inschatten van die behoefte ( product U2.1.2 ) moet dus daaraan voorafgegaan zijn . 
Die hadden als resultaat dat de Werkgroep_Jeugdgezondheidszorg in 2000-2001 uiteindelijk de totale zorg voor 0 tot 19-jarigen kon formuleren . 
De overgangen tussen voorlichting , advies , instructie en begeleiding zijn vloeiend , maar in alle gevallen staat het preventieve karakter van de interventie voorop . 
Dat betekent dat met_name begeleiding van beperkte duur en intensiteit zal zijn . 
Naar de aard van de voorlichting kan een onderscheid worden gemaakt in : 
Risicoreducerende voorlichting 
Ontwikkelingsondersteunende voorlichting 
Zorggerichte voorlichting 
Risicoreducerende voorlichting zal over het algemeen anticiperende voorlichting zijn , dat_wil_zeggen dat de voorlichting een primair preventief karakter heeft en vooruitloopt op risicovolle situaties die inherent zijn aan de ontwikkelingsfasen van het kind . 
Omdat de voorlichting over bekende risico's gaat , kan veel anticiperende voorlichting op een gestandaardiseerde wijze worden gegeven , zoals bijvoorbeeld voedingsvoorlichting ( Inspectie_voor_de_Gezondheidszorg / Voedingscentrum ) , veiligheidsvoorlichting ( Stichting_Consument_&_Veiligheid en Stichting_Wiegendood ) en voorlichting over mondverzorging ( Ivoren_Kruis ) . 
Hieronder volgen enkele voorbeelden van voorlichting waarvan het effect risicoreductie kan zijn : 
voeding 
verzorging 
veiligheid 
SOA-preventie 
anticonceptie 
verslaving ( roken , alcohol , drugs , gokken ) 
Ontwikkelingsondersteunende voorlichting is vooral gericht op het bevorderen van een goede ontwikkeling van het kind en op de rol van de ouders of verzorgers bij de opvoeding . 
Deze voorlichting kan anticiperend van aard zijn , maar ook secundair preventief gegeven worden op_basis_van signalen of vragen . 
In alle gevallen gaat het om maatwerk , omdat de voorlichtingsbehoefte van elke ouder verschillend is . 
Die zorg is vastgelegd in het Basistakenpakket_JGZ . 
De overgang tussen voorlichting en advies is vloeiend . 
Aangezien de voorlichting op maat gebeurt , dat wil dus zeggen afgestemd op de behoefte en vragen van de cliënt , geldt voor de activiteiten dat zij zeer variabel kunnen zijn naar onderwerp , tijd en plaats . 
Bij_wijze_van voorbeeld volgt hierna een lijst met onderwerpen waarop de ontwikkelingsondersteunende voorlichting onder andere betrekking kan hebben : 
verzorging 
Zorggerichte voorlichting is vooral gericht op het wegwijs maken van de ouders of verzorgers in het land van zorgvoorzieningen en op het geven van uitleg en toelichting op verwijzingen en behandelingen . 
Deze voorlichting wordt vrijwel altijd gegeven naar_aanleiding_van signalen of vragen of bij ( voorgenomen ) acties vanuit de jeugdgezondheidszorg . . 
