BASISTAKENPAKKET JEUGDGEZONDHEIDSZORG 0-19 JAAR 
Daarvoor is gekozen omdat gemeenten bij uitstek het lokale gezondheidsbeleid kunnen toespitsen op de specifieke gezondheidssituatie en zorgbehoeften van kinderen en jongeren in de eigen gemeente . 
Ook kunnen gemeenten rekening houden met het aanbod dat er op lokaal of regionaal niveau al is . 
Daarbij is van belang dat gemeenten zorg dragen voor goede afstemming en dat overlap en hiaten worden opgelost of voorkomen . 
Hoe de regie precies vorm krijgt , kan per gemeente verschillen en is afhankelijk van de specifieke situatie in een regio . 
Bij de uitvoering zullen veel verschillende partijen regelmatig met elkaar om de tafel zitten en maatregelen nemen , bijvoorbeeld de gemeente , het buurtwerk , peuterspeelzalen , scholen , thuiszorgorganisaties en GGD-en . 
De gevolgen van het Basistakenpakket_JGZ voor de praktijk 
Het uniform deel van het Basistakenpakket moet in heel Nederland aan alle kinderen op dezelfde wijze worden aangeboden . 
Daarnaast moet er regionaal extra aandacht komen voor kinderen die tot een risicogroep behoren . 
Dit wordt eenvoudiger door de invoering van een geautomatiseerd registratiesysteem voor het monitoren en signaleren van zorgbehoeften . 
Op_grond_van de gegevens in dat systeem , kan bijvoorbeeld worden vastgesteld of een groep kinderen een bepaalde zorgbehoefte heeft en of de gezondheidssituatie van een groep kinderen speciale zorg en andere maatregelen vraagt . 
INLEIDING 
Mogelijkheden voor de ontwikkeling van een dergelijk systeem worden onderzocht . 
De zorgverleners binnen de JGZ zullen concreet gaan ervaren dat de gemeente invloed heeft op het maatwerkdeel van het Basistakenpakket_JGZ . 
Vooral voor de zorgverleners uit de ouder- en kindzorg zal dit een duidelijke verandering zijn . 
De gemeente zal beleid vaststellen en maatregelen nemen . 
Vanzelfsprekend zal een gemeente daarbij zo veel mogelijk samenwerken met de uitvoerders van de JGZ want ze is afhankelijk van signalen vanuit het veld . 
Bovendien kan de gemeente alleen samen met het veld bepalen welke maatregelen moeten worden genomen . 
Tenslotte betekent de invoering van het Basistakenpakket dat zorgverleners in het veld meer nog dan tot nu toe hun vaardigheden op_het_gebied_van samenwerking en overleg moeten ontwikkelen en zich verder moeten professionaliseren . 
Zij zullen immers frequenter en intensiever dan tot nu toe gaan samenwerken met andere partijen . 
Zorgverleners moeten dan ook precies aangeven wat hun deskundigheid is en een heldere , professionele inbreng hebben in de diverse overlegvormen . 
Hiertoe zijn ondersteunende activiteiten gestart op_het_gebied_van standaarden , effectonderzoek en opleiding/scholing . 
OVERZICHT VAN DE PRODUCTGROEPEN 
Platform_JGZ 
Het ligt in_de_bedoeling een permanent Platform_JGZ in te stellen . 
In dat platform zullen alle JGZ-disciplines vertegenwoordigd zijn . 
Het platform zal met_name de kwaliteit van de jeugdgezondheidszorg bewaken en periodiek het Basistakenpakket op activiteitenniveau actualiseren . 
Uitgangspunten bij het actualiseren zijn veranderingen in de gezondheidssituatie en de zorgbehoefte van kinderen en jongeren van 0-19 en de wetenschappelijke ontwikkelingen . 
De herziening wordt vervolgens direct vertaald naar het Integraal_Dossier_JGZ , het individuele zorgdossier van elk in Nederland wonend kind . 
Zo kunnen zorgverleners vastleggen dat de activiteit bij het individuele kind is uitgevoerd . 
Naar één JGZ 
De invoering van het Basistakenpakket_JGZ en het versterken van de gemeentelijke regie zullen uiteindelijk leiden tot één integraal beleid voor jeugdgezondheidszorg voor kinderen en jeugdigen van 0 tot 19 jaar ] . 
Hoe het proces zal verlopen , is nu nog niet precies te zeggen . 
UNIFORM DEEL 
In elk geval zullen de thuiszorgorganisaties en de GGD-en in_het_kader_van de samenhang in de JGZ en optimale zorg voor kinderen en jongeren steeds nauwer gaan samenwerken en mogelijk één geheel gaan vormen . 
De invoering van het Basistakenpakket_JGZ is dan ook beslist geen afsluiting van een intensieve periode van overwegen , onderzoeken en besluiten . 
Het is vooral een belangrijke tussenstap in het totale proces richting samenhangende , integrale preventieve zorg voor kinderen en jeugdigen van 0 tot 19 jaar ] ] . 
Zo krijgen zij een goede basis mee voor een lang , gezond en zelfstandig leven . 
