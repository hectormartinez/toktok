Het gaat bij dit product niet om het zorgsysteem van de jeugdgezondheidszorg zelf , maar om het netwerk van voorzieningen voor kinderen en jeugdigen , zonder welk de jeugdgezondheidszorg niet goed kan functioneren . 
En hoe waarborgen we de continuïteit van die zorg ? 
De activiteit " sociale kaart " behelst dus het in_kaart brengen van dat netwerk van voorzieningen die jeugd als doelgroep hebben . 
Hoewel de sociale kaart plaatselijk grote verschillen kan vertonen , behoort het product wel tot het uniform deel van het Basistakenpakket_Jeugdgezondheidszorg , omdat het in beeld brengen van de sociale kaart altijd noodzakelijk is . 
