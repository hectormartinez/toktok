PRODUCT U1.1.1 ALGEMENE ANAMNESE 
Het veld werkt al met een integraal dossier JGZ voor 0-19_jarigen , waardoor de ontwikkeling van het kind longitudinaal gevolgd kan worden . 
Toelichting 
Het advies van de Werkgroep_Jeugdgezondheidszorg van 14_juli_1998 omvatte een drietal producten : Gezinssamenstelling , Erfelijke belasting en Voorgeschiedenis . 
Deze drie producten zijn op dit niveau te weinig onderscheidend en zijn daarom hier als activiteiten ondergebracht onder de noemer Algemene anamnese . 
Hoewel er geen landelijke standaard of protocol bestaat voor het afnemen van een algemene anamnese , wordt het Integraal_Dossier_JGZ als zodanig beschouwd . 
In de gebruikershandleiding wordt aangegeven c.q. zou moeten worden aangegeven welke gegevens over gezinssamenstelling , erfelijke belasting en voorgeschiedenis geregistreerd moeten worden . 
Bij voorgeschiedenis zou een duidelijk onderscheid gemaakt moeten worden in onveranderlijke gegevens die eenmalig bij het eerste contact geregistreerd moeten worden ( bijvoorbeeld zwangerschapsduur ) en gegevens die per voorliggende periode verschillend kunnen zijn ( bijvoorbeeld ziekenhuisopname ) . 
De ontwikkeling van standaarden vormt een krachtige impuls voor het ' evidence_based ' werken van de JGZ en draagt daarmee bij aan de kwaliteit . 
