Luotijuna törmäsi kuorma-autoon Ranskassa 
Pariisista Geneveen matkalla ollut ranskalainen TGV-luotijuna ja kuorma-auto törmäsivät toisiinsa aamulla Itä-Ranskassa lähellä Sveitsin rajaa . 
Onnettomuus tapahtui tasoristeyksessä 15 kilometriä Bourg-en-Bressen kaupungista . 
Erikoiskuljetukseen osallistuneen kuorma-auton kuljettaja sai onnettomuudessa surmansa , ja junan 200 matkustajasta 25 loukkaantui , yksi heistä vakavasti . 
Vakavasti loukkaantunut on poliisilähteiden mukaan junan kuljettaja . 
Ranskalaisten lähteiden mukaan juna suistui kiskoilta törmäyksen yhteydessä ja raahasi kuorma-autoa edellään . 
Kuitenkaan juna ei ajanut onnettomuushetkellä huippunopeuttaan eli 300 kilometriä tunnissa . 
TGV-junat ajavat toistaiseksi normaalia pikajunan nopeutta muilla rataosuuksilla kuin erityisesti nopeaa liikennettä varten suunnitelluilla aidatuilla osuuksilla , joissa ei ole tasoristeyksiä . 
