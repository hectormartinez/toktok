Tämän innostamana aion valmistaa makkaraa soveltaen lehden ohjeita . 
Oluen valmistamiseen verrattuna makkara on helppo : pienet mittavirheet esimerkiksi lihan määrässä eivät käy kohtalolliseksi toisin kuin pieni poikkeama hiivassa ja lämpötilassa kävi kohtaloksi oluen valmistamisessa . 
Makkaran ei myöskään tarvitse pulputtaa viikkoa huoneen nurkassa , jotta siitä pääsisi nauttimaan . 
Epäonnistumisen mahdollisuudet 
Vaikka makkara onkin helppo valmistaa , ei sen valmistus varmaankaan ole kuitenkaan mitään ruusuilla tanssimista . 
Makkaran valmstamiseen tarvitaan kuitenkin monenlaisia raaka-aineita ja välineitä . 
Raaka-aineiden suhteen ongelma ei liene kovin vaikea . 
Suolta , jonka avulla makkara lopulta saa lopullisen muotonsa , löytyy jokaisesta hyvin varustetusta kaupasta ja lihatiskikin on aika monessa kaupassa . 
Lihan suhteen ei kuitenkaan kelpaa mikä tahansa liha , vaan lihan on oltava erityisen rasvaista , jotta makkara onnistuu . 
Mitään kevytmakkaroita Filen jutuissa ei tulla valmistamaan . 
Raaka-aineiden kanssa auttaa myös luovuus : jos ohjeessa mainitaan jokin aine , jota ei löydy , sen voi korvata jollakin paremmin saatavilla olevalla aineella , jolloin lopputuloksena on persoonallisempi nautinto . 
Välineitä makkaran valmistamiseen tarvitaan myös joitakin . 
Kauha ja kattila eivät aivan riitä , koska makkaramassa pitää valmistaa jotenkin . 
Erityisen toimiva vempain tähän tehtävään olisi monitoimikone , joka vatkaisi taikinan sopivaksi . 
Lapsuudesta muistan kuinka äidin monitoimikone valmisti pullataikinan , kun vain laittoi ainekset kulhoon ja koneen päälle . 
Monitoimikonetta minulla ei siis ole , joten joudun soveltamaan ja kauhomaan taikinan esimerkiksi lusikalla , kauhalla tai muusinuijalla . 
Toisaalta onnistumisen ilman monitoimikonetta puolesta puhuu se , ettei esimerkiksi keskiajallakaan ollut niitä . 
Joten tämä ei tule olemaan kynnys tiellä makkaranvalmistukseen . 
Toinen suuri puute on lihamyllyn puute . 
Monissa lihamyllyissä on vakiovarusteena torvi tai jokin muu instrumentti , jonka avulla makkaramassa tungetaan suolen sisään . 
Tämä on varmasti sellainen tarvekalu , joka on ollut olemassa joskus keskiajallakin . 
Makkarassa ei välttämättä maistuisi niin hyvin tekemisen meininkin , jos valmistukseen olisi käytetty valmiita instrumentteja . 
Maailmassa on kuitenkin paljon objekteja , joissa on paksuudeltaan sellainen reikä johon makkara menee . 
Sopivilla virityksillä ja soveltavalla askartelulla saadaan aikaan kierrätysmakkarakone , jolla syntyy hyvä kyrsä . 
Tietyistä riskeistä huolimatta näen makkaraprojektin onnistumisen todennäköisempänä kuin oluen valmistamisen vuosi sitten . 
