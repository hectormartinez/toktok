EUROOPAN UNIONIN NEUVOSTO , joka ottaa huomioon Euroopan yhteisön perustamissopimuksen ja erityisesti sen 43 artiklan , ottaa huomioon kommission ehdotuksen ( 1 ) , ottaa huomioon parlamentin lausunnon ( 2 ) , sekä katsoo , että eläinlääkärintarkastuksista yhteisön sisäisessä kaupassa sisämarkkinoiden toteuttamista varten 11 päivänä joulukuuta 1989 annetussa neuvoston direktiivissä 89/662/ETY ( 3 ) ja eläinlääkärin ja kotieläinjalostustarkastuksista yhteisön sisäisessä , tiettyjen elävien eläinten ja tuotteiden kaupassa sisämarkkinoiden toteuttamista varten 26 päivänä kesäkuuta 1990 annetussa neuvoston direktiivissä 90/425/ETY ( 4 ) todetaan , että tuotantovaltiona olevan ja tarvittaessa todistukset laaditaan asianmukaisella tavalla , elävien eläinten ja eläintuotteiden sisämarkkinoiden yhdenmukaisen toiminnan varmistamiseksi jäsenvaltioiden on voitava täysin luottaa tuotanto- tai lähetyspaikalla laadittuun todistukseen , yksittäiset jäsenvaltiot eivät voi toteuttaa tätä tavoitetta ; tämän vuoksi on hyväksyttävä yhteiset säännöt toimivaltaisten viranomaisten ja todistuksia myöntävien viranomaisten velvollisuuksista eläimiä ja eläintuotteita koskevien todistusten laadinnassa yhteisön lainsäädännön mukaisesti , on varmistauduttava siitä , että kolmansien maiden todistuksia myöntävien viranomaisten noudattamat säännöt ja periaatteet tarjoavat vähintään tässä direktiivissä säädettyjä takuita vastaavat takuut , ja on toteutettava tehokkaita toimenpiteitä väärien tai harhaanjohtavien todistusten välttämiseksi , 
ON ANTANUT TÄMÄN DIREKTIIVIN : 
1 artikla Tässä direktiivissä vahvistetaan säännöt , joita noudatetaan eläinlääkintäalaa koskevassa lainsäädännössä vaaditun todistuksen myöntämisessä . 
2 artikla 
1. Tässä direktiivissä tarkoitetaan : 
" eläinlääkintäalaa koskevalla lainsäädännöllä " : direktiivin 89/662/ETY liitteessä A ja direktiivin 90/425/ETY liitteissä A ja B tarkoitettua lainsäädäntöa ; 
" todistuksia myöntävillä viranomaisilla " : virkaeläinlääkäriä tai , mikäli eläinlääkintäalaa koskevassa lainsäädännössä niin säädetään , mitä tahansa muuta henkilöä , jolle toimivaltainen viranomainen on myöntänyt luvan mainitussa lainsäädännössä tarkoitettujen todistusten allekirjoittamiseen . 
2. Edellä 1 kohdassa olevien määritelmien lisäksi sovelletaan soveltuvin osin direktiivien 89/662/ETY ja 90/425/ETY 2 artiklassa esitettyjä määritelmiä . 
3 artikla 
1. Viranomaisen on huolehdittava siitä , että todistuksia myöntävillä viranomaisilla on riittävä eläinlääkintäalaa koskevan lainsäädännön tuntemus niiden eläinten ja eläintuotteiden osalta , joista heidän on annettava todistus ja että he ovat yleisesti ottaen perillä todistusten myöntämisessä ja laatimisessa noudatettavista säännöistä ja mikäli tarpeen , niiden tutkimusten , testien ja tarkastusten luonteesta ja laajuudesta , joita asiasta on suoritettava ennen todistusten laatimista . 
2. Todistuksia myöntävät viranomaiset eivät saa laatia todistuksia seikoista , joita he eivät henkilökohtaisesti tunne tai joita he eivät voi tarkistaa . 
3. Todistuksia myöntävät viranomaiset eivät saa allekirjoittaa tyhjiä tai epätäydellisiä asiakirjoja eivätkä sellaisia eläimiä tai tuotteita koskevia todistuksia , joita he eivät ole tarkastaneet tai jotka eivät enää ole heidän valvonnassaan . 
Jos todistus allekirjoitetaan toisen todistuksen tai asiakirjan perusteella , todistuksia myöntävällä viranomaisella on oltava kyseinen asiakirja hallussaan ennen allekirjoittamista . 
4. Nämä säännökset eivät estä virkaeläinlääkäriä allekirjoittamasta todistusta sellaisista tiedoista , jotka a ) toimivaltaisen viranomaisen valtuuttama , mainitun eläinlääkärin valvonnassa toimiva henkilö on todennut oikeiksi tämän artiklan 1-3 kohdan mukaisesti , sikäli kuin hän voi tarkistaa näiden tietojen paikkansapitävyyden , tai b ) on saatu valvontaohjelmien puitteissa , virallisesti tunnustettuihin laadunvalvontajärjestelmiin liittyvänä tai tartuntatautien valvontajärjestelmän välityksellä niissä tapauksissa , joissa se on eläinlääkintäalaa koskevan lainsäädännön mukaan sallittua . 
5. Tämän artiklan soveltamista koskevat mahdolliset yksityiskohtaiset säännöt voidaan antaa 7 artiklassa säädetyn menettelyn mukaisesti . 
4 artikla 
1. Toimivaltaisten viranomaisten on toteutettava kaikki tarvittavat toimenpiteet todistusten laadinnan luotettavuuden turvaamiseksi . 
Heidän on erityisesti huolehdittava siitä , että heidän nimeämänsä todistuksia myöntävät viranomaiset ovat a ) asemassa , joka takaa heidän puolueettomuutensa ja sen , että heille ei koidu minkäänlaista suoraa kaupallista hyötyä niistä eläimistä tai tuotteista , joista he laativat todistuksen tai yrityksistä tai laitoksista , joista eläimet ovat peräisin , b ) tietoisia jokaisen allekirjoittamansa todistuksen sisällöstä . 
2. Todistukset on laadittava vähintään yhdellä todistuksia myöntävien viranomaisten ymmärtämällä kielellä ja vähintään yhdellä kohdemaan virallisista kielistä , siten kuin yhteisön lainsäädännössä on edellytetty . 
3. Kunkin toimivaltaisen viranomaisen on kyettävä osoittamaan todistuksen ja sen myöntäneen viranomaisen välinen yhteys ja huolehdittava siitä , että kustakin tämän nimellä myönnetystä todistuksesta on saatavissa jäljennös mainitun viranomaisen määräämän ajanjakson ajan . 
5 artikla 
1. Jäsenvaltioiden on otettava käyttöön ja määrättävä toteutettavaksi tarpeelliset valvontatoimenpiteet virheellisten tai mahdollisesti harhaanjohtavien todistusten myöntämisen estämiseksi ja eläinlääkintäalaa koskevan lainsäädännön mukaisiin tarkoituksiin myönnetyiltä näyttävien todistusten laittoman laatimisen tai käytön estämiseksi . 
