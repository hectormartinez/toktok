Makkara - oluen kaveri 
Makkara on monen oluen ystävän suosikkiruokaa . 
Makkara , jota perinteisesti syödään , on jauhosta ja lihaan verrattavista raaka-aineista valmistettua pötköä . 
Maku on kohtalainen , mutta yleensä parempaakin on syöty . 
Makkaran valmistuksesta ei kukaan tiedä – eikä varmasti edes halua tietää – mitään . 
