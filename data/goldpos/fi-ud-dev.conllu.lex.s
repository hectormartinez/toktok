Arvoisa puhemies , komission ehdotus pienentää perunatärkkelyksen tuotantokiintiöitä on olennainen osa Agenda 2000 -ohjelmaa , joka hyväksyttiin Berliinissä . 
Siksi on hyvä asia , että me käsittelemme tätä asiaa tänään kiireellisenä . 
Perunatärkkelyksen markkinajärjestelyt liittyvät kiinteästi viljan markkinajärjestelyihin . 
Se on oikeutettua , koska perunatärkkelyksen tuottajat toimivat merkittävältä osin samoilla markkinoilla kuin viljasta valmistettavan tärkkelyksen tuottajat . 
Onkin erittäin tärkeää , että perunatärkkelyksen tuet ovat samalla tasolla kuin viljalle annettavat tuet . 
Komission alkuperäiset ehdotukset häiritsivät perunatärkkelyksen ja viljan välistä tasapainoa vakavasti . 
Näillä ehdotuksilla olisi ollut tuhoisat seuraukset perunatärkkelyksen tuottajien tuloille . 
Komissio halusi korvata hintojen laskun vain 44 %:lla . 
Neuvosto korjasi tämän oikeutetusti nostamalla tulonlisät 75 %:iin hintojen laskusta . 
Jotta tästä ei aiheutuisi muutoksia talousarvioon , neuvosto yhdisti tämän päätöksen esillä olevaan asetukseen kiintiöiden pienentämisestä . 
Tästä ratkaisusta on alalle hyötyä . 
Kiintiöiden pienentäminen sopii myös erinomaisesti nykyisiin kysyntä- ja tarjontasuhteisiin perunatärkkelysmarkkinoilla . 
Muutamia vuosia sitten eurooppalaisia kiintiöitä laajennettiin poliittisista syistä . 
Uudelle Saksalle annettiin 110 000 tonnin kiintiö . 
Viimeksi EU:n jäseniksi liittyneet valtiot saivat miltei 170 000 tonnin kiintiön . 
Tästä on aiheutunut tiettyä ylituotantoa Euroopan markkinoilla . 
Lyhyesti , ehdotus kiintiöiden pienentämisestä ansaitsee kaiken tuen . 
